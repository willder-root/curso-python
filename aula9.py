# operadores aritiméticos

adicao = 1 + 20
print('Adição:', adicao)

subtracao = 23 - 4
print('Subtração:', subtracao)

mutiplicacao = 2 * 3
print('mutiplicação:', mutiplicacao)

divisao = 10 / 5 # sempre retorna um valor do tipo float
print('Divisão:', divisao)

divisao_inteira = 10 // 2.2 # no resultado caso for um float sempre vai ter como casa decimal o zero
print("Divisão inteira:", divisao_inteira)

exponenciacao = 2 ** 10
print("Exponenciação:", exponenciacao)

modulo = 55 % 2 # resto da divisão
print("Modulo:", modulo)